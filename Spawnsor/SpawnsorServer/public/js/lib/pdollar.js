function getPointsAroundCircle(numPoints, radius, centerX, centerY, strokeId)
{
	var points = new Array();
	var slice = 2 * Math.PI / numPoints;
	for (var i = 0; i < numPoints; i++) {
		var angle = slice * i;
		var newX = centerX + radius * Math.cos(angle)
    var newY = centerY + radius * Math.sin(angle)
    var point = new Point(newX, newY, strokeId);
    points.push(point);
	}
	return points;
}



